	/**
	 * Request 의 데이터를 VO 배열로 변환하여 반환.
	 * @param request
	 * @return SearchContiInfoVO[]
	 */
	public SearchContiInfoVO[] fromRequestGrid(HttpServletRequest request) {
		return fromRequestGrid(request, "");
	}

	/**
	 * Request 넘어온 여러 건 DATA를 VO Class 에 담는다. 
	 * @param request
	 * @param prefix
	 * @return SearchContiInfoVO[]
	 */
	public SearchContiInfoVO[] fromRequestGrid(HttpServletRequest request, String prefix) {
		SearchContiInfoVO model = null;
		
		String[] tmp = request.getParameterValues(prefix + "ibflag");
  		if(tmp == null)
   			return null;

  		int length = request.getParameterValues(prefix + "ibflag").length;
  
		try {
			String[] contiCd = (JSPUtil.getParameter(request, prefix	+ "conti_cd", length));
			String[] cntNm = (JSPUtil.getParameter(request, prefix	+ "cnt_nm", length));
			String[] ibflag = (JSPUtil.getParameter(request, prefix	+ "ibflag", length));
			String[] subContiCd = (JSPUtil.getParameter(request, prefix	+ "sub_conti_cd", length));
			String[] cntCd = (JSPUtil.getParameter(request, prefix	+ "cnt_cd", length));
			String[] pagerows = (JSPUtil.getParameter(request, prefix	+ "pagerows", length));
			
			for (int i = 0; i < length; i++) {
				model = new SearchContiInfoVO();
				if (contiCd[i] != null)
					model.setContiCd(contiCd[i]);
				if (cntNm[i] != null)
					model.setCntNm(cntNm[i]);
				if (ibflag[i] != null)
					model.setIbflag(ibflag[i]);
				if (subContiCd[i] != null)
					model.setSubContiCd(subContiCd[i]);
				if (cntCd[i] != null)
					model.setCntCd(cntCd[i]);
				if (pagerows[i] != null)
					model.setPagerows(pagerows[i]);
				models.add(model);
			}

		} catch (Exception e) {
			return null;
		}
		return getSearchContiInfoVOs();
	}

	/**
	 * VO 배열을 반환
	 * @return SearchContiInfoVO[]
	 */
	public SearchContiInfoVO[] getSearchContiInfoVOs(){
		SearchContiInfoVO[] vos = (SearchContiInfoVO[])models.toArray(new SearchContiInfoVO[models.size()]);
		return vos;
	}
	
