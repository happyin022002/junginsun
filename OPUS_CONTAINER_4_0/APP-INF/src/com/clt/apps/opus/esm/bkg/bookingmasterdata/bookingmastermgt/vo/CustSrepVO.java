	public CustSrepVO[] fromRequestGrid(HttpServletRequest request) {
		return fromRequestGrid(request, "");
	}

	/**
	 * Request 넘어온 여러 건 DATA를 VO Class 에 담는다. 
	 * @param request
	 * @param prefix
	 * @return CustSrepVO[]
	 */
	public CustSrepVO[] fromRequestGrid(HttpServletRequest request, String prefix) {
		CustSrepVO model = null;
		
		String[] tmp = request.getParameterValues(prefix + "ibflag");
  		if(tmp == null)
   			return null;

  		int length = request.getParameterValues(prefix + "ibflag").length;
  
		try {
			String[] ofcCd = (JSPUtil.getParameter(request, prefix	+ "ofc_cd", length));
			String[] ibflag = (JSPUtil.getParameter(request, prefix	+ "ibflag", length));
			String[] sort = (JSPUtil.getParameter(request, prefix	+ "sort", length));
			String[] srepNm = (JSPUtil.getParameter(request, prefix	+ "srep_nm", length));
			String[] srepCd = (JSPUtil.getParameter(request, prefix	+ "srep_cd", length));
			String[] pagerows = (JSPUtil.getParameter(request, prefix	+ "pagerows", length));
			
			for (int i = 0; i < length; i++) {
				model = new CustSrepVO();
				if (ofcCd[i] != null)
					model.setOfcCd(ofcCd[i]);
				if (ibflag[i] != null)
					model.setIbflag(ibflag[i]);
				if (sort[i] != null)
					model.setSort(sort[i]);
				if (srepNm[i] != null)
					model.setSrepNm(srepNm[i]);
				if (srepCd[i] != null)
					model.setSrepCd(srepCd[i]);
				if (pagerows[i] != null)
					model.setPagerows(pagerows[i]);
				models.add(model);
			}

		} catch (Exception e) {
			return null;
		}
		return getCustSrepVOs();
	}

	/**
	 * VO 배열을 반환
	 * @return CustSrepVO[]
	 */
	public CustSrepVO[] getCustSrepVOs(){
		CustSrepVO[] vos = (CustSrepVO[])models.toArray(new CustSrepVO[models.size()]);
		return vos;
	}
	
