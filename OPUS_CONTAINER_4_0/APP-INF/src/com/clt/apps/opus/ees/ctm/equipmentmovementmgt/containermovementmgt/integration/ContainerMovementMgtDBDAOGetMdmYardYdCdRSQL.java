public class ContainerMovementMgtDBDAOGetMdmYardYdCdRSQL implements ISQLTemplate{

	private StringBuffer query = new StringBuffer();
	
	Logger log =Logger.getLogger(this.getClass());
	
	/** Parameters definition in params/param elements */
	private HashMap<String,String[]> params = null;
	
	/**
	  * <pre>
	  * 배치 실행시 IEM인경우 EVENT Yard 값을 MDM_YARD테이블에서 조회한다.
	  * </pre>
	  */
	public ContainerMovementMgtDBDAOGetMdmYardYdCdRSQL(){
		setQuery();
		params = new HashMap<String,String[]>();
		String tmp = null;
		String[] arrTmp = null;
		tmp = java.sql.Types.VARCHAR + ",N";
		arrTmp = tmp.split(",");
		if(arrTmp.length !=2){
			throw new IllegalArgumentException();
		}
		params.put("event_yard",new String[]{arrTmp[0],arrTmp[1]});

		query.append("/*").append("\n"); 
		query.append("Path : com.clt.apps.opus.ees.ctm.equipmentmovementmgt.containermovementmgt.integration").append("\n"); 
		query.append("FileName : ContainerMovementMgtDBDAOGetMdmYardYdCdRSQL").append("\n"); 
		query.append("*/").append("\n"); 
	}
	
	public String getSQL(){
		return query.toString();
	}
	
	public HashMap<String,String[]> getParams() {
		return params;
	}

	/**
	 * Query 생성
	 */
	public void setQuery(){
		query.append("SELECT YD_CD AS EVENT_YARD " ).append("\n"); 
		query.append("FROM MDM_YARD " ).append("\n"); 
		query.append("WHERE 1=1" ).append("\n"); 
		query.append("	AND MODI_YD_CD=@[event_yard]  " ).append("\n"); 
		query.append("	AND ROWNUM=1" ).append("\n"); 

	}
}