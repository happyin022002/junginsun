	/**
	 * Request 넘어온 여러 건 DATA를 VO Class 에 담는다. 
	 * @param request
	 * @param prefix
	 * @return SdmsPortStayingDatesVO[]
	 */
	public SdmsPortStayingDatesVO[] fromRequestGrid(HttpServletRequest request, String prefix) {
		SdmsPortStayingDatesVO model = null;
		
		String[] tmp = request.getParameterValues(prefix + "ibflag");
  		if(tmp == null)
   			return null;

  		int length = request.getParameterValues(prefix + "ibflag").length;
  
		try {
			String[] stayingDt = (JSPUtil.getParameter(request, prefix	+ "staying_dt", length));
			String[] ofcCd = (JSPUtil.getParameter(request, prefix	+ "ofc_cd", length));
			String[] ibflag = (JSPUtil.getParameter(request, prefix	+ "ibflag", length));
			String[] pagerows = (JSPUtil.getParameter(request, prefix	+ "pagerows", length));
			
			for (int i = 0; i < length; i++) {
				model = new SdmsPortStayingDatesVO();
				if (stayingDt[i] != null)
					model.setStayingDt(stayingDt[i]);
				if (ofcCd[i] != null)
					model.setOfcCd(ofcCd[i]);
				if (ibflag[i] != null)
					model.setIbflag(ibflag[i]);
				if (pagerows[i] != null)
					model.setPagerows(pagerows[i]);
				models.add(model);
			}

		} catch (Exception e) {
			return null;
		}
		return getSdmsPortStayingDatesVOs();
	}

	/**
	 * VO 배열을 반환
	 * @return SdmsPortStayingDatesVO[]
	 */
	public SdmsPortStayingDatesVO[] getSdmsPortStayingDatesVOs(){
		SdmsPortStayingDatesVO[] vos = (SdmsPortStayingDatesVO[])models.toArray(new SdmsPortStayingDatesVO[models.size()]);
		return vos;
	}
	
